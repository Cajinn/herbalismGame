// All recipes for a given (species, teil) combination
export function recipesFor(species, teil) {
  return recipes.filter((r) => r.species === species && r.teil === teil);
}

// All recipes playable at a given station (method.station must match)
export function recipesForStation(station, methods) {
  return recipes.filter((r) => methods[r.method]?.station === station);
}
